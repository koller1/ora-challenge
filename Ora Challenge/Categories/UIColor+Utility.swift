import UIKit

extension UIColor {
    
    static func oraOrange() -> UIColor {
        return UIColor(red: 0.98, green: 0.71, blue: 0.18, alpha: 1.00)
    }
    
    static func oraBlack() -> UIColor {
        return UIColor(red: 0.01, green: 0.01, blue: 0.01, alpha: 1.00)
    }
    
    static func oraGrey() -> UIColor {
        return UIColor(red: 0.91, green: 0.91, blue: 0.92, alpha: 1.00)
    }
    
}